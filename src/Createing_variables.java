public class Createing_variables {
    public static void main (String[] args){
        // char
        char var1 = 'a';
        // we cannot give more than one value to char like string
        String var2 = "Hi my name is xyz";
        // we can also give a numerical value to the char and string but cannot perform any operation on them
        boolean var3 = true;
        int var4 = 9;
        double var5 = 9.8;
        float var6 = 9.5f;
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
        System.out.println(var4);
        System.out.println(var5);
        System.out.println(var6);
    }
}